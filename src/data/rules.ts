/** 地域区分（日本全国を気候で8つに分けたもの） */
export type Region = 1 | 2 | 3 | 4 | 5 | 6 | 7 | 8;

/** 建物の部位ごとの断熱基準 */
export interface InsulationRule {
  /** 部位の名前（日本語） */
  label: string;
  /** 基準となる熱抵抗値 R [m²·K/W]。これ以上あれば適合。0 = 基準なし */
  minR: number;
  /** ユーザー向けの説明 */
  description: string;
}

/** 窓の基準 */
export interface WindowRule {
  /** 基準となる熱貫流率 U [W/(m²·K)]。これ以下であれば適合。Infinity = 基準なし */
  maxU: number;
  /** ユーザー向けの説明 */
  description: string;
}

/** ある地域の仕様基準まるごと */
export interface RegionRules {
  regionNumber: Region;
  regionName: string;
  /** 代表的な都市（ユーザーが地域を選びやすくするため） */
  exampleCities: string[];
  /** 部位別の断熱基準 */
  insulation: {
    roof: InsulationRule;  // 屋根または天井
    wall: InsulationRule;  // 外壁
    floor: InsulationRule; // 床（その他の部分）
  };
  /** 窓の基準 */
  window: WindowRule;
}

